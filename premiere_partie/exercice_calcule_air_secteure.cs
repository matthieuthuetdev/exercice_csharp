 namespace demarrage
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int angle;
            int r;
            double air;
            Console.WriteLine("saisisez le rayon du secture : ");
            r = int.Parse(Console.ReadLine());
            Console.WriteLine("saisisez maintenant l'angle du secteur : ");
            angle = int.Parse(Console.ReadLine());
            air = (Math.PI * Math.Pow(r, 2) * angle) / 360;
            Console.WriteLine("l'aire du secteure est de " + air);
        }

    }
}